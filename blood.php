<h2 align="left" style="color:#09C;"><b>BLOOD</b></h2>
<H3>Blood is a specialized bodily fluid that delivers necessary substances to the body’s cells such as nutrients and oxygen, and transports waste products away from those same cells. Blood comprises more than 8% of the body weight of a healthy individual.
On an average, every adult person has about 5 to 6 litres of blood. The major component of blood is a fluid called plasma in which are suspended cellular elements. These are Red Blood Cells or RBCs, White Blood Cells or WBCs and platelets.
</H3>
</div>
<div class="row">
<h2 align="left" style="color:#09C;"><b>BLOOD GROUPS</b></h2>
<H3>
Although all blood is made of the same basic elements, not all blood is alike. In fact, there are eight different common blood types, which are determined by the presence or absence of certain antigens substances that can trigger an immune response if they are foreign to the body. Since some antigens can trigger a patient’s immune system to attack the transfused blood, safe blood transfusions depend on careful blood typing and cross-matching.<BR /><br />
There are four major blood groups determined by the presence or absence of two antigens A and B on the surface of red blood cells:<br /><br />
<b>Group A</b> has only the A antigen on red cells (and B antibody in the plasma)<br /><br />
<b>Group B</b> has only the B antigen on red cells (and A antibody in the plasma)<br /><br />
<b>Group AB</b> has both A and B antigens on red cells (but neither A nor B antibody in the plasma)<br /><br />
<b>Group O</b> has neither A nor B antigens on red cells (but both A and B antibody are in the plasma)<br /><br />
In addition to the A and B antigens, there is a third antigen called the Rh factor, which can be either present (+) or absent. In general, Rh negative blood is given to Rh-negative patients, and Rh positive blood or Rh negative blood may be given to Rh positive patients.<br /><br />
The universal red cell donor has Type O negative blood type.<br /><br />
The universal plasma donor has Type AB positive blood type.
</H3>
