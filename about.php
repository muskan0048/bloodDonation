<h1 align="center" style="color:#09C;"> <b>||ABOUT US||</b></h1>
<hr width="100%" />
<h3>The Rotary Blood Bank is the second largest public service initiative taken up by the Clubs in Rotary District 3010, the first being the Institute Rotary Cancer Hospital at AIIMS. The Blood Bank is a 15,000 sq. ft centrally air-conditioned state-of-the-art facility set up on 1500 sq. meters in Tughlakabad Institutional Area, New Delhi. We raised over Rs. 5 crores for this dream project. Rotary International gave a grant of Rs. 2 crores while the Central Government and the Delhi Government gave Rs. 53 lakh as support for the project. Considering the need for a Blood Bank run on voluntary blood donations in the context of the increasing threat of HIV/AIDS, Hepatits B & C, five Members of the Parliament, namely Dr. Karan Singh, Dr. Sahib Singh Verma, Smt. Ambika Soni, Dr. A.R.Kidwai and Shri F.S. Narimaneachgave Rs. 20 lakhs to the Rotary Blood Bank from their Local Area Development Fund. One of our Rotary members Shri Trilok nath, I.P. a former Inspector General of Police donated Rs 80 lakhs in the memory of his wife Smt. Sarojini Nath.
Our mission statement is “No one shall suffer in Delhi and neighbouring areas for want of blood.”</h3>
 
<h3>We have acquired most sophisticated Blood Banking equipment worth over Rs. 2 crores. We have highly qualified staff to run the Blood Bank. Our main emphasis is on encouraging the medical fraternity to use blood components instead of whole blood.By use of components, optimum utilization of blood can be obtained and 3 to 4 lives can be helped with just one unit of blood. At Rotary Blood Bank, we do not ask for replacement donors who could as well be paid donors. We want to discourage the clandestine sale of blood. It is well-known that several semi nourished people, drug addicts and other persons short of money for smack, etc. donate blood for money.</h3>
 
<h3>We are organizing voluntary blood donation camps in and around Delhi to encourage the voluntary blood donation movement. As a result, high quality blood, fully tested against all infections is available to people in life threatening situations. We have a modest processing charge for providing blood/blood components to those in need. Any person who cannot afford the processing charge is given blood free of charge. The service charge is intended to take care of running of the blood bank, which includes salary for a staff of 80 persons, cost of donor bags, testing equipment, consumables, electricity charges and several other operational costs.</h3>
 
<h3>Rotary Blood Bank was inaugurated by the Union Minister Shri L.K. Advani on March 1, 2001 in the presence of the Union Health Minister, Lt. Governor, Chief Minister and the Health Minister of NCT of Delhi who were our guests of honour.</h3>
 
<h3>Ours is a commitment to serve society. Nearly 1000 Rotarians and other socially sensitive persons/institutions have contributed for this humanitarian project.</h3><hr width="100%" />
